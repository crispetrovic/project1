        Example 1: <em>(if you struggle with jealousy)</em><br>
        <strong>I have</strong> everything I need, I am content with what I have, and I celebrate the blessings of others.<br>
    </p>
    <p>
        Example 2: <em>(if you struggle with apathy)</em><br>
        <strong>I am</strong> motivated to take responsibility for my emotions and improve my personal relationships. <br>
    </p>
    <p>
        <strong>Tip:</strong> Be specific.<br> 
        The more detailed you can make your affirmations, the more effective they will be. :)
