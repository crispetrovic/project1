    <h3> Create A Positive Affirmation </h3>
    
    <p>	Use positive affirmations to weaken negative thought patterns. <br>
        The more you reinforce positive thoughts, the more emotionally resilient <br>
        you will become. 
    </p>
    <p> Identify a resource, feeling, achievement, state of being, etc., <br>
